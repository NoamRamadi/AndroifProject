package com.example.gamemaster;

public class GameData {
    public String name;
    public String description;
    public String genre;
    public float rate;


    public GameData(String name, String description, String genre, float rate) {
        this.name = name;
        this.description = description;
        this.genre = genre;
        this.rate = rate;
    }


    public GameData(){}
}
